// 대입 연산자 연습
public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 20;
        System.out.println("a = " + a);
        a += 10;
        System.out.println("a = " + a);
        a -= 10;
        System.out.println("a = " + a);
        a *= 2;
        System.out.println("a = " + a);
        a /= 2;
        System.out.println("a = " + a);
        a %= 3;
        System.out.println("a = " + a);

    }
}
